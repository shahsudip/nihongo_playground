import json
import os

def insert_grammar(batch_data):
    grammar_file = "power_drill_grammar_data.json"
    
    with open(grammar_file, "r", encoding="utf-8") as f:
        data = json.load(f)
        
    for new_chapter in batch_data:
        chapter_obj = {
            "id": new_chapter["id"],
            "title": new_chapter["title"],
            "type": "questions-only",
            "description": f"{new_chapter['title']} grammar drills.",
            "passages": []
        }
        
        if "mondai_1" in new_chapter:
            chapter_obj["passages"].append({
                "id": f"{new_chapter['id']}-mondai1",
                "title": "問題1",
                "mondaiHeader": "つぎの文の（　）に入れるのに最もよいものを、１・２・３・４から一つえらびなさい。",
                "questions": new_chapter["mondai_1"]
            })
            
        if "mondai_2" in new_chapter:
            chapter_obj["passages"].append({
                "id": f"{new_chapter['id']}-mondai2",
                "title": "問題2",
                "mondaiHeader": "つぎの文の ★ に入る最もよいものを、１・２・３・４から一つえらびなさい。",
                "questions": new_chapter["mondai_2"]
            })
            
        if "mondai_3" in new_chapter:
            chapter_obj["passages"].append({
                "id": f"{new_chapter['id']}-mondai3",
                "title": "問題3",
                "mondaiHeader": "つぎの文章を読んで、文章の中に入る最もよいものを、１・２・３・４から一つえらびなさい。",
                "text": new_chapter["mondai_3"].get("text", ""),
                "questions": new_chapter["mondai_3"]["questions"]
            })
            
        # Check if exists and replace, else append
        existing_idx = next((i for i, ch in enumerate(data["chapters"]) if ch["id"] == chapter_obj["id"]), -1)
        if existing_idx >= 0:
            data["chapters"][existing_idx] = chapter_obj
        else:
            data["chapters"].append(chapter_obj)
            
    with open(grammar_file, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    print("Grammar data updated successfully.")

if __name__ == "__main__":
    insert_grammar(batch_data)
